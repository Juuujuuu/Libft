#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*s2;

	if (!s)
		return (NULL);
	i = 0;
	s2 = malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (s2 == NULL)
		return (NULL);
	while (s[i])
	{
		s2[i] = s[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
